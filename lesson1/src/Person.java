
public class Person {
    String name;
    int age;

    // Parameterized Constructor
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Default Constructor (Java provides it if not explicitly defined)
    public Person() {
        // Default constructor with no parameters
        // You can provide default values or leave it empty
    }

}
